// Go program to illustrate the use of strings
package main

import "fmt"

func main() {
	// str variable which stores strings
	str := "GeeksforGeeks"

	// Display the length of the string
	fmt.Printf("Length of the string is: %d\n", len(str))

	// Display the string
	fmt.Printf("String is: %s\n", str)

	// Display the type of the str variable
	fmt.Printf("Type os str is: %T\n", str)
}
