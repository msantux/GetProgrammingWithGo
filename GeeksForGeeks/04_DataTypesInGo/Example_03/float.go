// Go programming to illustrate the use of floating-point numbers
package main

import "fmt"

func main() {
	a := 20.45
	b := 34.89

	// Subtraction of two floating-point numbers
	c := b - a

	// Display the result
	fmt.Printf("Result is: %f\n", c)

	// Display the type of the c variable
	fmt.Printf("The type of c is: %T\n", c)
}
