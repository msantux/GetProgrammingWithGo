// Go program to illustrate concept of variable
package main

import "fmt"

func main() {
	// Multiple variable of the same type are declared and initialized in the single line
	var myVariable1, myVariable2, myVariable3 int = 2, 454, 67

	//Display the values of the variables
	fmt.Printf("The value of myVariable1 is: %d\n", myVariable1)
	fmt.Printf("The value of myVariable2 is: %d\n", myVariable2)
	fmt.Printf("The value of myVariable3 is: %d\n", myVariable3)
}
